public class ScoreItem
{
    private String holeNumber;
    private String holeShotCount;

    public String getHoleNumber()
    {
        return holeNumber;
    }

    public void setHoleNumber(String holeNumber)
    {
        this.holeNumber = holeNumber;
    }

    public String getHoleShotCount()
    {
        return holeShotCount;
    }

    public void setHoleShotCount(String holeShotCount)
    {
        this.holeShotCount = holeShotCount;
    }



}
